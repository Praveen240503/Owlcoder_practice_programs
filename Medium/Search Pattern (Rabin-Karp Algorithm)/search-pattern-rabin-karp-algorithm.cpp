class Solution
{
    public:
        vector <int> search(string pattern, string text)
        {
            //code here.
            vector<int>tr;
            int pos = text.find(pattern);
            while(pos >= 0)
            {
                tr.push_back(pos+1);
                pos = text.find(pattern,pos+1);
            }
          
            return tr;
        }
     
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        string S, pat;
        cin >> S >> pat;
        Solution ob;
        vector <int> res = ob.search(pat, S);
        for (int i : res) cout << i << " ";
        cout << endl;
    }
    return 0;
}

